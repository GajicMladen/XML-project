package main.java.com.xws.a1document.dto;

public class TAdresaDTO {
	private String ulica;
	private int broj;
	private String grad;
	private int postanski_kod;
	
	public TAdresaDTO() {
		
	}

	public String getUlica() {
		return ulica;
	}

	public void setUlica(String ulica) {
		this.ulica = ulica;
	}

	public int getBroj() {
		return broj;
	}

	public void setBroj(int broj) {
		this.broj = broj;
	}

	public String getGrad() {
		return grad;
	}

	public void setGrad(String grad) {
		this.grad = grad;
	}

	public int getPostanski_kod() {
		return postanski_kod;
	}

	public void setPostanski_kod(int postanski_kod) {
		this.postanski_kod = postanski_kod;
	}
	
	
}
