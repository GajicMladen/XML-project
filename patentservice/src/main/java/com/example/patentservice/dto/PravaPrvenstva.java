package com.example.patentservice.dto;

public class PravaPrvenstva {
    private String datumPodnosenja;
    private String brojPrijave;
    private String dvoslovnaOznaka;
    
	public String getDatumPodnosenja() {
		return datumPodnosenja;
	}
	public void setDatumPodnosenja(String datumPodnosenja) {
		this.datumPodnosenja = datumPodnosenja;
	}
	public String getBrojPrijave() {
		return brojPrijave;
	}
	public void setBrojPrijave(String brojPrijave) {
		this.brojPrijave = brojPrijave;
	}
	public String getDvoslovnaOznaka() {
		return dvoslovnaOznaka;
	}
	public void setDvoslovnaOznaka(String dvoslovnaOznaka) {
		this.dvoslovnaOznaka = dvoslovnaOznaka;
	}
    
    
}
